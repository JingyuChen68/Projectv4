function priceForCategory(category: ChipCategory, chip: ChipSpec, index: number) {
  const base = {
    "Wireless MCU": 2.35,
    "General Purpose MCU": 1.1,
    "Performance MCU": 5.4,
    "Ultra Low Power MCU": 1.85,
    "Motor Control MCU": 3.25,
    "Secure MCU": 3.95,
  }[category];

  const memoryAdder = Math.min(5, chip.flashKB / 1024) * 0.38 + Math.min(4, chip.sramKB / 256) * 0.24;
  const performanceAdder = Math.min(4, chip.clockMHz / 250) * 0.55;
  const packageAdder = chip.package.includes("BGA") ? 0.75 : 0.25;
  return Number((base + memoryAdder + performanceAdder + packageAdder + (index % 5) * 0.18).toFixed(2));
}

